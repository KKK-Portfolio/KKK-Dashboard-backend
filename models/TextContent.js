const mongoose = require("mongoose");

const textContentSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const TextContent = mongoose.model("TextContent", textContentSchema);

module.exports = TextContent;
